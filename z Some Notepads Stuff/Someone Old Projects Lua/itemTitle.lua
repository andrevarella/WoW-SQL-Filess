--------------------------------------------
	-- 			CONFIGURATION		  --
--------------------------------------------
	local itemId = 123450
	local titleId = 200
--------------------------------------------


function titleOnItemUse(event, player, item, target)
	player:SetKnownTitle( titleId )
	player:RemoveItem( itemId, 1 )
	player:SendBroadcastMessage( "Title learned" )
end
RegisterItemEvent( itemId, 2, titleOnItemUse )

